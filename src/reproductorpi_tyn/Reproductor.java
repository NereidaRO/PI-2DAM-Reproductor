    private void btnTogglePlayPauseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTogglePlayPauseActionPerformed
        if (btnTogglePlayPause.isSelected()) {
            isReproducing = true;
            lbEstado.setText("Estado: Reproduciendo");
            btnTogglePlayPause.setIcon(new ImageIcon(getClass().getResource(imgPausa)));

            if (!timer.isRunning()) {
                timer.start();
            }

        } else {
            isReproducing = false;
            lbEstado.setText("Estado: Pausado");
            btnTogglePlayPause.setIcon(new ImageIcon(getClass().getResource(imgPlay)));
        }
    }//GEN-LAST:event_btnTogglePlayPauseActionPerformed

    private void btnStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStopActionPerformed
        reprCounter = 0;
        isReproducing = false;

        timer.stop();

        pbReproduction.setValue(0);
        lbContador.setText("00:00");
        lbEstado.setText("Estado: Detenido");

        btnTogglePlayPause.setSelected(false);
        btnTogglePlayPause.setIcon(new ImageIcon(getClass().getResource(imgPlay)));
    }//GEN-LAST:event_btnStopActionPerformed

