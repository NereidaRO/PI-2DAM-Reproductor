    public Reproductor() {
        initComponents();

        pbReproduction.setMaximum(duracion); // le asignamos un valor tope al ProgressBar

        /**
         * Creamos un timer cuya finalidad es ejecutarse cada segundo para ir
         * avanzando el ProgressBar y el jlabel contador.
         */
        timer = new javax.swing.Timer(1000, (e) -> {
            if (isReproducing) {
                reprCounter++;
                /**
                 * Si llega al final se pausa automaticamente, cambia el
                 * lbEstado y el btnToggle a false y el icono.
                 */
                if (reprCounter >= duracion) {
                    reprCounter = duracion;
                    timer.stop();
                    isReproducing = false;
                    lbEstado.setText("Estado: Terminado");
                    btnTogglePlayPause.setSelected(false);
                    btnTogglePlayPause.setIcon(new ImageIcon(getClass().getResource(imgPlay)));
                }
                /**
                 * Aqui actualizamos el progressbar y el lbContador cada
                 * segundo.
                 */
                pbReproduction.setValue(reprCounter);
                lbContador.setText("00:" + String.format("%02d", reprCounter));
            }
        });
    }

